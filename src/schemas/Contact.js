export const types = `
  type Website {
    website: String!
    tag: String
    verified: Boolean!
    autogenerated: Boolean!
    description: String
    priority: Int
    unitid: Unit
   }

   type Email {
     unitid: Unit!
     email: String!
     verified: Boolean!
     autogenerated: Boolean!
     description: String
     priority: Int
     tag: String
   }

  type Phone {
    number: String!
    verified: Boolean!
    autogenerated: Boolean!
    description: String
    priority: Int
    tag: String
    unitid: Unit!
  }

  type Address {
    id: Int!
    country: String!
    address: JSON
    description: String
    priority: Int
    tag: String
    unitid: Unit!
  }

  input AddressInput {
    description: String
    country: String
    street: String
    city: String
    state: String
    zip: String
    priority: Int
    tag: String
  }
`;

export const queries = `
  fetchAddresses: [Address]!
  # A function only usable by Vipfy admins
  fetchUserAddresses(unitid: Int!): [Address]!
`;

export const mutations = `
  adminCreateAddress(addressData: AddressInput!, unitid: Int!): Response!
  # Without the id parameter, a new address will be generated. Otherwise it will be updated.
  updateAddress(id: Int, country: String, address: AddressInput, description: String, priority: Int): Response!

  adminUpdateAddress(addressData: AddressInput!, id: Int!): Response!
  adminDeleteAddress(id: Int!): Response!

  adminCreateEmail(email: String!, unitid: Int!): Response!
  adminDeleteEmail(email: String!,unitid: Int!): Response!
`;
